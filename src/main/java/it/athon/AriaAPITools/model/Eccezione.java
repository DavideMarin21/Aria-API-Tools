package it.athon.AriaAPITools.model;

public class Eccezione {

    private String codiceEccezione;
    private String descEccezione;
    private String nomeCampo;
    private String descEccezioneCampo;
    private String codiceEccezioneCampo;
    private String ROI;

    public Eccezione() {}

    public String getCodiceEccezione() {
        return codiceEccezione;
    }

    public void setCodiceEccezione(String codiceEccezione) {
        this.codiceEccezione = codiceEccezione;
    }

    public String getDescEccezione() {
        return descEccezione;
    }

    public void setDescEccezione(String descEccezione) {
        this.descEccezione = descEccezione;
    }

    public String getNomeCampo() {
        return nomeCampo;
    }

    public void setNomeCampo(String nomeCampo) {
        this.nomeCampo = nomeCampo;
    }

    public String getDescEccezioneCampo() {
        return descEccezioneCampo;
    }

    public void setDescEccezioneCampo(String descEccezioneCampo) {
        this.descEccezioneCampo = descEccezioneCampo;
    }

    public String getCodiceEccezioneCampo() {
        return codiceEccezioneCampo;
    }

    public void setCodiceEccezioneCampo(String codiceEccezioneCampo) {
        this.codiceEccezioneCampo = codiceEccezioneCampo;
    }

    public String getROI() {
        return ROI;
    }

    public void setROI(String rOI) {
        ROI = rOI;
    }

    
    
}
