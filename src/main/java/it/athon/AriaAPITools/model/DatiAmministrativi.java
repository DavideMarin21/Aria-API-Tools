package it.athon.AriaAPITools.model;

public class DatiAmministrativi {
    
    private String dataEmissione;
    private String tipologiaPrescrizione;
    private String tipoRicetta;
    private String tipoModulo;
    private String tipoVisita;
    private String flagSuggerita;
    private String flagRicEl;
    private String classePriorita;
    private String flagRipetibile;
    private String generaPromemoria;
    
    public String getDataEmissione() {
        return dataEmissione;
    }
    public void setDataEmissione(String dataEmissione) {
        this.dataEmissione = dataEmissione;
    }
    public String getTipologiaPrescrizione() {
        return tipologiaPrescrizione;
    }
    public void setTipologiaPrescrizione(String tipologiaPrescrizione) {
        this.tipologiaPrescrizione = tipologiaPrescrizione;
    }
    public String getTipoRicetta() {
        return tipoRicetta;
    }
    public void setTipoRicetta(String tipoRicetta) {
        this.tipoRicetta = tipoRicetta;
    }
    public String getTipoModulo() {
        return tipoModulo;
    }
    public void setTipoModulo(String tipoModulo) {
        this.tipoModulo = tipoModulo;
    }
    public String getTipoVisita() {
        return tipoVisita;
    }
    public void setTipoVisita(String tipoVisita) {
        this.tipoVisita = tipoVisita;
    }
    public String getFlagSuggerita() {
        return flagSuggerita;
    }
    public void setFlagSuggerita(String flagSuggerita) {
        this.flagSuggerita = flagSuggerita;
    }
    public String getFlagRicEl() {
        return flagRicEl;
    }
    public void setFlagRicEl(String flagRicEl) {
        this.flagRicEl = flagRicEl;
    }
    public String getClassePriorita() {
        return classePriorita;
    }
    public void setClassePriorita(String classePriorita) {
        this.classePriorita = classePriorita;
    }
    public String getFlagRipetibile() {
        return flagRipetibile;
    }
    public void setFlagRipetibile(String flagRipetibile) {
        this.flagRipetibile = flagRipetibile;
    }
    public String getGeneraPromemoria() {
        return generaPromemoria;
    }
    public void setGeneraPromemoria(String generaPromemoria) {
        this.generaPromemoria = generaPromemoria;
    }
   
}
